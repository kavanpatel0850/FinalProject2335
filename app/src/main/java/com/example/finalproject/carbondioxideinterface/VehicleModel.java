package com.example.finalproject.carbondioxideinterface;

public class VehicleModel {

    String id;
    String modelName;
    String year;
    String vehicleMakerId;
    String vehicleMakerName;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getModelName() {
        return modelName;
    }

    public void setModelName(String modelName) {
        this.modelName = modelName;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getVehicleMakerId() {
        return vehicleMakerId;
    }

    public void setVehicleMakerId(String vehicleMakerId) {
        this.vehicleMakerId = vehicleMakerId;
    }

    public String getVehicleMakerName() {
        return vehicleMakerName;
    }

    public void setVehicleMakerName(String vehicleMakerName) {
        this.vehicleMakerName = vehicleMakerName;
    }
}
